function printProducts(array) {
    let myNode = document.getElementById('wrapper');
    myNode.innerHTML = '';
    const products_wrapper = document.getElementById("wrapper");



    array.forEach((array, ind) => {
        const div_wrapper = document.createElement("div");
        div_wrapper.innerHTML =
            ` <div class="forWrapper">
            <img src="${array.photo}" class="photo1">
            <p class="priceShop">$ ${array.price}</p>
            <p class="sizeShop">Size: ${array.size}</p>
            <button class="but">To card</button>
        </div>
      `


        products_wrapper.appendChild(div_wrapper);

    })

};



